num1 = int(input("Insira um numero: "))
num2 = int(input("Insira outro numero: "))

print("Soma: {}, Subtração: {}, Multiplicação: {}, Divisão: {}, Divisão inteira: {}, Resto da divisão: {}, Exponenciação: {}, Maior: {}, Menor: {}".format(num1+num2, num1-num2, num1*num2, num1/num2, num1//num2, num1%num2, num1**num2, max(num1, num2), min(num1, num2)))